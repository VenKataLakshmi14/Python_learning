x = 10
y = 3.5

result = x + y
print(f"Implicit conversion result (x + y): {result}")

x_str = "123"
x_int = int(x_str)
print(f"Converted string '123' to integer: {x_int}")

x_float = float(x_int)
print(f"Converted integer 123 to float: {x_float}")

y_int = int(y)
print(f"Converted float 3.5 to integer: {y_int}")
